package br.com.exercicioGft.entities;

public class Refeicao {
	private int periodo;
	private int tipo_prato;
	private String prato;

	public int getPeriodo() {
		return periodo;
	}

	public void setPeriodo(int periodo) {
		this.periodo = periodo;
	}

	public int getTipo_prato() {
		return tipo_prato;
	}

	public void setTipo_prato(int tipo_prato) {
		this.tipo_prato = tipo_prato;
	}

	public String getPrato() {
		return prato;
	}

	public void setPrato(String prato) {
		this.prato = prato;
	}

}
